package logicalPractice;

public class starProg7 {

	public static void main(String[] args) {

		for(int row=0;row<=7;row++)
		{
			 if(row==0)
			{
				System.out.println(" /^^^^^^^^^^^/");
			}
		    else if(row==1)
			{
				System.out.println(" |----------|");
			}
			else if(row==2)
			{
				System.out.println(" |  ~    ~  |");
			}
			else if(row==3)
			{
				System.out.println(" |-(*)||(*)-|");
			}
			else if(row==4)
			{
				System.out.println("{|   <00>   |}");
			}
			else if(row==5)
			{
				System.out.println(" |  ~~~~~~  |");
			}
			else if(row==6)
			{
				System.out.println(" |   <==>   |");
			}
			else if(row==7)
			{
				System.out.println(" |__________|");
			}
			
		}
	}

}
